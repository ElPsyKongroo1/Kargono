void TextureManager::CreateTextures()
{
    
    smileyFace = new Texture("Resources/Default-Resources/Textures/Original/awesomeface.png", "texture_diffuse");
    smileyFaceSpec = new Texture("Resources/Default-Resources/Textures/SpecularMap/awesomeface.png", "texture_specular");
    crate = new Texture("Resources/Default-Resources/Textures/Original/Metal_Box.png", "texture_diffuse");
    crateSpec = new Texture("Resources/Default-Resources/Textures/SpecularMap/Metal_Box.png", "texture_specular");
}


void TextureManager::DestroyTextures()
{
    delete smileyFace;
    delete smileyFaceSpec;
    delete crate;
    delete crateSpec;
}


