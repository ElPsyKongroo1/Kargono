 /*============================================================================================================================================================================================
  * Instantiate/Delete GLCamera objects
  *============================================================================================================================================================================================*/

void CameraManager::CreateCameras()
{
    CreateFlyCamera();
    CreateStatic2DCamera();
}
void CameraManager::DestroyCameras()
{
    delete flyCamera;
    delete static2DCamera;
    flyCamera = nullptr;
    static2DCamera = nullptr;
}
/*============================================================================================================================================================================================
 * Initializes GLCamera Structs with specified values
 *============================================================================================================================================================================================*/
void CameraManager::CreateFlyCamera()
{
    flyCamera = new GLCamera(glm::vec3(0.0f, 0.0f, 3.0f), glm::vec3(0.0f, 0.0f, -1.0f), glm::vec3(0.0f, 1.0f, 0.0f),
        glm::vec3(-90.0f, 0.0f, 0.0f), 4.0f, GLCamera::PERSPECTIVE,
        glm::vec2(0.0f, 0.0f), glm::vec2(0.0f, 0.0f), glm::vec2(0.1f, 100.0f), 45.0f,
        (float)Resources::currentApplication->renderer->screenDimension.x / (float)Resources::currentApplication->renderer->screenDimension.y,
        0.1f);

}

void CameraManager::CreateStatic2DCamera()
{
    static2DCamera = new GLCamera(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, 0.0f, -1.0f), glm::vec3(0.0f, 1.0f, 0.0f),
        glm::vec3(-90.0f, 0.0f, 0.0f), 1.0f, GLCamera::ORTHOGRAPHIC,
        glm::vec2(-5.0f, 5.0f), glm::vec2(-3.75, 3.75), glm::vec2(0.1f, 100.0f), 45.0f,
        (float)Resources::currentApplication->renderer->screenDimension.x / (float)Resources::currentApplication->renderer->screenDimension.y,
        0.1f);
}